# Import the 'operator' module to use its functions.
import operator

# Assign the value 5 to the variable number1.
number1 = 5
# Assign the value 3 to the variable number2.
number2 = 3

# Use the 'add' function from the 'operator' module to add number1 and number2.  Store the result in the 'sum' variable.
sum = operator.add(number1, number2)

# Print the sum to the console.
print("The sum is:", sum)